package OOPs_InheritanceAndOverriding;

public class Fruits extends Vegetables {
	
	
	
	@Override
	public void green() {
		System.out.println("Fruits - green");
	}
	
	public void yellow() {
		System.out.println("Fruits - yellow");
	}
	
	
	public void ripe () {
		System.out.println("Fruits - ripe");
	}

}
